void vTimerStop( uint8_t ucTimer ){
    if( ucTimer == TIMER_SEARCH ){
        TIM6_CR1.CEN = 0;
    }
}

/*********************************************************************
 * Timer6 Prescaler :575; Preload = 62499; Actual Interrupt Time = 500 ms
*********************************************************************/
void vTimerSearchLed(){
    RCC_APB1ENR.TIM6EN = 1;
    TIM6_CR1.CEN = 0;
    TIM6_PSC = 575;
    TIM6_ARR = 62499;
    NVIC_IntEnable(IVT_INT_TIM6);
    TIM6_DIER.UIE = 1;
    TIM6_CR1.CEN = 1;
}

