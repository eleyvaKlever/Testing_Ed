void vDongleLoop(){
    vUARTRxInit();

    #ifdef FACTORY_DONGLE
        UART1_Write_Text( "APP_START\r\n" );
    #else
        STM_LOGI( "[ APP ] Start\r\n", NULL );
    #endif

    while( 1 ){
        if( ucUARTDataReady == 1 ){
            ucUARTDataReady = 0;
            vUARTRxMessage();
        }
        if( ucRF4463GetIRQFlag() == 1 ){
            // STM_LOGV( "[ SYS ] RF4463\r\n", NULL );
            vRF4463MesageHandler();
            vRF4463ResetIRQFlag();
        }
    }
}
