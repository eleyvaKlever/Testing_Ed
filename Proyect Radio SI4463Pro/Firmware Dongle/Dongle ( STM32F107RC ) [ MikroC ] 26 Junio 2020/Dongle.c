void vDongleLoop(){
    NVIC_IntEnable( IVT_INT_EXTI15_10 );

    vUARTRxInit();

    #ifdef FACTORY_DONGLE
        UART1_Write_Text( "APP_START\r\n" );
    #else
        UART1_Write_Text( "[ APP ] Dongle Start\r\n" );
    #endif

    while( 1 ){
        if( ucUARTDataReady == 1 ){
            ucUARTDataReady = 0;
            vUARTRxMessage();
        }
        if( ucRadioEvent == 1 ){
            ucRadioEvent = 0;   
            vRFRxMessage();
        }
    }
}
